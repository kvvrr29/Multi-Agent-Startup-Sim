import React from 'react';
import { useSettingsStore } from '../store/useSettingsStore';
import { useAIDebugStore } from '../store/useAIDebugStore';
import { AlertTriangle, Cpu, Zap } from 'lucide-react';

/**
 * Returns the current AI mode as a simple descriptor object.
 * Use this hook anywhere you need to know if real AI is running.
 */
export function useAIMode() {
  const { aiModeEnabled, apiKey } = useSettingsStore();
  const generationSources = useAIDebugStore(s => s.generationSources);

  const isGeminiConfigured = aiModeEnabled && !!apiKey?.trim();

  // If at least one agent used Gemini successfully, we've confirmed live AI
  const hasLiveOutput = Object.values(generationSources).some(s => s === 'Gemini');
  const hasFallbackOutput = Object.values(generationSources).some(s => s === 'Fallback');

  const mode = isGeminiConfigured ? 'Gemini' : 'Simulator';
  const reason = !aiModeEnabled
    ? 'AI Mode is disabled in Settings'
    : !apiKey?.trim()
    ? 'Gemini API Key Missing'
    : null;

  return { mode, reason, isGeminiConfigured, hasLiveOutput, hasFallbackOutput };
}

/**
 * Prominent banner shown on ProjectCreation when API key is missing.
 */
export function AIStatusBanner() {
  const { mode, reason } = useAIMode();

  if (mode === 'Gemini') return null;

  return (
    <div style={{
      display: 'flex', alignItems: 'flex-start', gap: '12px',
      padding: '14px 16px', borderRadius: '10px', marginBottom: '1.5rem',
      background: 'rgba(245, 158, 11, 0.08)',
      border: '1px solid rgba(245, 158, 11, 0.4)',
    }}>
      <AlertTriangle size={18} color="#f59e0b" style={{ flexShrink: 0, marginTop: '1px' }} />
      <div style={{ fontSize: '0.85rem', lineHeight: 1.5 }}>
        <div style={{ fontWeight: 700, color: '#f59e0b', marginBottom: '2px' }}>
          Running in Simulator Mode
        </div>
        <div style={{ color: 'var(--text-secondary)' }}>
          Gemini AI is not configured. The platform is currently using <strong>fallback templates</strong>, not real AI.
          {reason && <span style={{ color: 'var(--text-muted)', marginLeft: '4px' }}>— {reason}.</span>}
        </div>
      </div>
    </div>
  );
}

/**
 * Small inline badge used in the Dashboard header / Blueprint sections.
 */
export function AIModeBadge({ style }) {
  const { mode, reason } = useAIMode();
  const isGemini = mode === 'Gemini';

  return (
    <div
      title={reason || 'Gemini AI active'}
      style={{
        display: 'inline-flex', alignItems: 'center', gap: '5px',
        padding: '3px 10px', borderRadius: '20px', fontSize: '0.72rem', fontWeight: 700,
        background: isGemini ? 'rgba(16, 185, 129, 0.12)' : 'rgba(245, 158, 11, 0.12)',
        border: `1px solid ${isGemini ? 'rgba(16,185,129,0.35)' : 'rgba(245,158,11,0.35)'}`,
        color: isGemini ? '#10b981' : '#f59e0b',
        cursor: 'default',
        ...style
      }}
    >
      {isGemini ? <Zap size={11} /> : <Cpu size={11} />}
      {isGemini ? 'Gemini Active' : 'Simulator Mode'}
    </div>
  );
}

/**
 * Confidence label that changes wording based on whether real AI ran.
 */
export function ConfidenceLabel({ value, agentRole }) {
  const generationSources = useAIDebugStore(s => s.generationSources);
  const source = generationSources[agentRole];

  const isSimulator = source === 'Fallback' || source === null;

  const label = isSimulator ? 'Template Quality' : 'AI Confidence';
  const color = value === 'High'
    ? 'var(--success)'
    : value === 'Medium'
    ? 'var(--warning)'
    : 'var(--danger)';

  return (
    <div style={{
      fontSize: '0.75rem', display: 'flex', alignItems: 'center', gap: '4px',
      background: 'rgba(0,0,0,0.2)', padding: '2px 8px', borderRadius: '12px'
    }}>
      <span style={{ color: 'var(--text-muted)' }}>{label}:</span>
      <span style={{ color, fontWeight: 500 }}>{value}</span>
      {isSimulator && (
        <span title="This output was generated by the Fallback Simulator, not Gemini AI."
          style={{ color: '#f59e0b', fontSize: '0.65rem', marginLeft: '2px' }}>⚠</span>
      )}
    </div>
  );
}
